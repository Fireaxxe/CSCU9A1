/**
 * A program that draws a shape using xs
 * @
 * @ 11/11/16
 */

public class PrintXs
{
    public static void main(String[] args)
    {
        for (int i = 1; i <= 5 ; i++)
        {
            for (int j = 1; j <= i; j++)
            {
                System.out.print("X");
            }
            System.out.print("\n");
        }
    }
}
